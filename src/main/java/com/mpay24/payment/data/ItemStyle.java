package com.mpay24.payment.data;

public class ItemStyle {
	private String numberStyle;
	private String productNrStyle;
	private String descriptionStyle;
	private String quantityStyle;
	private String itemPriceStyle;
	private String priceStyle;
	private String packageStyle;
	public String getNumberStyle() {
		return numberStyle;
	}
	public void setNumberStyle(String numberStyle) {
		this.numberStyle = numberStyle;
	}
	public String getProductNrStyle() {
		return productNrStyle;
	}
	public void setProductNrStyle(String productNrStyle) {
		this.productNrStyle = productNrStyle;
	}
	public String getDescriptionStyle() {
		return descriptionStyle;
	}
	public void setDescriptionStyle(String descriptionStyle) {
		this.descriptionStyle = descriptionStyle;
	}
	public String getQuantityStyle() {
		return quantityStyle;
	}
	public void setQuantityStyle(String quantityStyle) {
		this.quantityStyle = quantityStyle;
	}
	public String getItemPriceStyle() {
		return itemPriceStyle;
	}
	public void setItemPriceStyle(String itemPriceStyle) {
		this.itemPriceStyle = itemPriceStyle;
	}
	public String getPriceStyle() {
		return priceStyle;
	}
	public void setPriceStyle(String priceStyle) {
		this.priceStyle = priceStyle;
	}
	public String getPackageStyle() {
		return packageStyle;
	}
	public void setPackageStyle(String packageStyle) {
		this.packageStyle = packageStyle;
	}
	
}
