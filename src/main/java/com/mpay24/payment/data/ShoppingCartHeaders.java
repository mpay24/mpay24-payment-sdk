package com.mpay24.payment.data;

public class ShoppingCartHeaders {
	private String header;
	private String numberHeader;
	private String productNrHeader;
	private String descriptionHeader;
	private String packageHeader;
	private String quantityHeader;
	private String itemPriceHeader;
	private String priceHeader;
	private String taxHeader;
	private String shippingCostsHeader;
	private String subTotalHeader;
	private String discountHeader;
	
	
	public String getHeader() {
		return header;
	}
	public void setHeader(String header) {
		this.header = header;
	}
	public String getNumberHeader() {
		return numberHeader;
	}
	public void setNumberHeader(String numberHeader) {
		this.numberHeader = numberHeader;
	}
	public String getProductNrHeader() {
		return productNrHeader;
	}
	public void setProductNrHeader(String productNrHeader) {
		this.productNrHeader = productNrHeader;
	}
	public String getDescriptionHeader() {
		return descriptionHeader;
	}
	public void setDescriptionHeader(String descriptionHeader) {
		this.descriptionHeader = descriptionHeader;
	}
	public String getPackageHeader() {
		return packageHeader;
	}
	public void setPackageHeader(String packageHeader) {
		this.packageHeader = packageHeader;
	}
	public String getQuantityHeader() {
		return quantityHeader;
	}
	public void setQuantityHeader(String quantityHeader) {
		this.quantityHeader = quantityHeader;
	}
	public String getItemPriceHeader() {
		return itemPriceHeader;
	}
	public void setItemPriceHeader(String itemPriceHeader) {
		this.itemPriceHeader = itemPriceHeader;
	}
	public String getPriceHeader() {
		return priceHeader;
	}
	public void setPriceHeader(String priceHeader) {
		this.priceHeader = priceHeader;
	}
	public String getTaxHeader() {
		return taxHeader;
	}
	public void setTaxHeader(String taxHeader) {
		this.taxHeader = taxHeader;
	}
	public String getShippingCostsHeader() {
		return shippingCostsHeader;
	}
	public void setShippingCostsHeader(String shippingCostsHeader) {
		this.shippingCostsHeader = shippingCostsHeader;
	}
	public String getSubTotalHeader() {
		return subTotalHeader;
	}
	public void setSubTotalHeader(String subTotalHeader) {
		this.subTotalHeader = subTotalHeader;
	}
	public String getDiscountHeader() {
		return discountHeader;
	}
	public void setDiscountHeader(String discountHeader) {
		this.discountHeader = discountHeader;
	}
}
